#!/usr/bin/env python3
"""Generate a Bug Markdown document from Maestro test results.

Parses report.xml for the first failure/error message, collects git info,
and writes a structured Bug document to persistent-assets/governance/issues/.

Usage:
    python tac-skills/tac-maestro-run/scripts/tac_create_bug_from_maestro_result.py \
        --result-dir build/maestro-results \
        --device-id <id> \
        [--flow-path ...] [--apk-path ...] [--output-dir ...]

Cross-platform: works on Windows, Linux, macOS.
"""

import argparse
import os
import subprocess
import xml.etree.ElementTree as ET
from datetime import datetime
from pathlib import Path


# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

DEFAULT_FLOW_PATH = os.path.join("persistent-assets", "automated-testing", "_baseline", "flows")
DEFAULT_APK_PATH = os.path.join("app", "build", "outputs", "apk", "debug", "app-debug.apk")
DEFAULT_OUTPUT_DIR = os.path.join("persistent-assets", "governance", "issues")


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def get_first_failure_message(report_path: str) -> str:
    """Extract the first failure or error message from a JUnit report.xml."""
    if not os.path.isfile(report_path):
        return "Maestro did not produce report.xml"

    try:
        tree = ET.parse(report_path)
        root = tree.getroot()

        # Search for <failure> first, then <error>
        for tag in ("failure", "error"):
            for elem in root.iter(tag):
                message = elem.get("message", "")
                if not message.strip():
                    message = (elem.text or "").strip()
                if not message:
                    message = f"Maestro flow failed (<{tag}> node found, no message)"
                # Collapse newlines for single-line display
                return " ".join(message.splitlines()).strip()

    except ET.ParseError as exc:
        return f"Failed to parse Maestro JUnit report: {exc}"

    return "Maestro flow failed, but no failure node was found in report.xml"


def get_git_value(*git_args: str) -> str:
    """Run a git command and return its stdout, or 'unknown' on failure."""
    try:
        result = subprocess.run(
            ["git", *git_args],
            capture_output=True, text=True, check=True,
        )
        return result.stdout.strip()
    except (subprocess.CalledProcessError, FileNotFoundError):
        return "unknown"


# ---------------------------------------------------------------------------
# Bug document template
# ---------------------------------------------------------------------------

BUG_TEMPLATE = """\
---
created: {created}
bug_id: {bug_id}
status: open
priority: P1
source: maestro
---

# Bug Tracking - {bug_id}

## Basic Info

| Field | Value |
|------|------|
| Bug ID | {bug_id} |
| Created At | {created} |
| Status | open |
| Priority | P1 |
| Source | Maestro selftest |
| Flow | {flow_path} |
| DeviceId | {device_id} |
| Git Branch | {branch} |
| Git Commit | {commit} |
| APK | {apk_path} |

## Problem

### Symptom
Maestro selftest failed: {failure_message}

### Expected Behavior
The quickstart path should complete as expected and pass Maestro assertions.

### Repro Steps
1. Build and install the Debug APK.
2. Run: `python tac-skills/tac-maestro-run/scripts/tac_run_maestro_selftest.py --device-id {device_id} --flow-path {flow_path}`
3. Check the failed step in the Maestro report.

### Logs
See {logcat_path}.

### Automation Artifacts
- JUnit: {report_path}
- Maestro artifacts: {result_dir}
- Logcat: {logcat_path}

### Impact
The path covered by this Maestro flow. Check properties.sourceSpec and properties.path in the flow for details.

---

Generated by tac_create_bug_from_maestro_result.py at {created}.
"""


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Generate a Bug document from Maestro test results.",
    )
    parser.add_argument("--result-dir", required=True,
                        help="Directory containing Maestro results (report.xml, logcat.txt).")
    parser.add_argument("--device-id", required=True,
                        help="adb device serial used for the test run.")
    parser.add_argument("--flow-path", default=DEFAULT_FLOW_PATH,
                        help="Maestro flow path that was executed.")
    parser.add_argument("--apk-path", default=DEFAULT_APK_PATH,
                        help="Path to the debug APK used.")
    parser.add_argument("--output-dir", default=DEFAULT_OUTPUT_DIR,
                        help="Output directory for Bug documents.")
    return parser.parse_args()


def main() -> None:
    args = parse_args()

    os.makedirs(args.output_dir, exist_ok=True)

    now = datetime.now()
    bug_id = f"BUG{now.strftime('%Y%m%d%H%M%S')}"
    created = now.strftime("%Y-%m-%d %H:%M:%S")

    report_path = os.path.join(args.result_dir, "report.xml")
    logcat_path = os.path.join(args.result_dir, "logcat.txt")
    failure_message = get_first_failure_message(report_path)

    commit = get_git_value("rev-parse", "HEAD")
    branch = get_git_value("branch", "--show-current")

    bug_path = os.path.join(args.output_dir, f"{bug_id}.md")

    body = BUG_TEMPLATE.format(
        created=created,
        bug_id=bug_id,
        flow_path=args.flow_path,
        device_id=args.device_id,
        branch=branch,
        commit=commit,
        apk_path=args.apk_path,
        failure_message=failure_message,
        logcat_path=logcat_path,
        report_path=report_path,
        result_dir=args.result_dir,
    )

    Path(bug_path).write_text(body, encoding="utf-8")
    print(f"Created Bug document: {bug_path}", flush=True)


if __name__ == "__main__":
    main()
